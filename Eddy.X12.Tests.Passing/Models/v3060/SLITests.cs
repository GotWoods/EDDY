	[Fact]
	public void Parse_ShouldReturnCorrectObject()
	{
		string x12Line = "SLI*t*7*8*8*6*zO*y*0*E*iu*l*gg*I*9*3*8*7*z*5*3r*j*a";

		var expected = new SLI_SpecificLoanInformation()
		{
			LoanTypeCode = "t",
			MonetaryAmount = 7,
			MonetaryAmount2 = 8,
			InterestRate = 8,
			LoanRateTypeCode = "6",
			DateTimePeriodFormatQualifier = "zO",
			DateTimePeriod = "y",
			ReferenceIdentification = "0",
			YesNoConditionOrResponseCode = "E",
			DateTimePeriodFormatQualifier2 = "iu",
			DateTimePeriod2 = "l",
			DateTimePeriodFormatQualifier3 = "gg",
			DateTimePeriod3 = "I",
			MonetaryAmount3 = 9,
			Quantity = 3,
			Quantity2 = 8,
			Quantity3 = 7,
			YesNoConditionOrResponseCode2 = "z",
			YesNoConditionOrResponseCode3 = "5",
			DateTimePeriodFormatQualifier4 = "3r",
			DateTimePeriod4 = "j",
			PaymentMethodCode = "a",
		};

		var actual = Map.MapObject<SLI_SpecificLoanInformation>(x12Line, MapOptionsForTesting.x12DefaultEndsWithNewline);
		Assert.Equivalent(expected, actual);
	}

