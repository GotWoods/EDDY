			AssignedIdentification = "7",
			ChangeOrResponseTypeCode = "jf",
			QuantityOrdered = 6,
			QuantityLeftToReceive = 6,
			UnitOrBasisForMeasurementCode = "s7",
			UnitPrice = 3,
			BasisOfUnitPriceCode = "XD",
			ProductServiceIDQualifier = "8J",
			ProductServiceID = "b",
			ProductServiceIDQualifier2 = "PS",
			ProductServiceID2 = "r",
			ProductServiceIDQualifier3 = "6L",
			ProductServiceID3 = "V",
			ProductServiceIDQualifier4 = "DF",
			ProductServiceID4 = "2",
			ProductServiceIDQualifier5 = "ql",
			ProductServiceID5 = "F",
			ProductServiceIDQualifier6 = "35",
			ProductServiceID6 = "H",
