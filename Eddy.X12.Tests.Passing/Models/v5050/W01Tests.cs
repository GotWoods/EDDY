	[Fact]
	public void Parse_ShouldReturnCorrectObject()
	{
		string x12Line = "W01*8*Fp*Amz4TMwMFWJt*Am*1*oV*i*W4*J*h*M*171665*s*It*JB*a*71";

		var expected = new W01_LineItemDetailWarehouse()
		{
			Quantity = 8,
			UnitOrBasisForMeasurementCode = "Fp",
			UPCCaseCode = "Amz4TMwMFWJt",
			ProductServiceIDQualifier = "Am",
			ProductServiceID = "1",
			ProductServiceIDQualifier2 = "oV",
			ProductServiceID2 = "i",
			FreightClassCode = "W4",
			RateClassCode = "J",
			CommodityCodeQualifier = "h",
			CommodityCode = "M",
			PalletBlockAndTiers = 171665,
			WarehouseLotNumber = "s",
			ProductServiceConditionCode = "It",
			ProductServiceIDQualifier3 = "JB",
			ProductServiceID3 = "a",
			SpecialServicesCode = "71",
		};

		var actual = Map.MapObject<W01_LineItemDetailWarehouse>(x12Line, MapOptionsForTesting.x12DefaultEndsWithNewline);
		Assert.Equivalent(expected, actual);
	}

