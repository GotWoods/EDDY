	[Fact]
	public void Parse_ShouldReturnCorrectObject()
	{
		string x12Line = "W01*7*z5*etBPUasExyC5*z5*8*Po*n*CB*v*r*i*136875*p*FM*aW*f*af";

		var expected = new W01_LineItemDetailWarehouse()
		{
			Quantity = 7,
			UnitOrBasisForMeasurementCode = "z5",
			UPCCaseCode = "etBPUasExyC5",
			ProductServiceIDQualifier = "z5",
			ProductServiceID = "8",
			ProductServiceIDQualifier2 = "Po",
			ProductServiceID2 = "n",
			FreightClassCode = "CB",
			RateClassCode = "v",
			CommodityCodeQualifier = "r",
			CommodityCode = "i",
			PalletBlockAndTiers = 136875,
			WarehouseLotNumber = "p",
			ProductServiceConditionCode = "FM",
			ProductServiceIDQualifier3 = "aW",
			ProductServiceID3 = "f",
			SpecialServicesCode = "af",
		};

		var actual = Map.MapObject<W01_LineItemDetailWarehouse>(x12Line, MapOptionsForTesting.x12DefaultEndsWithNewline);
		Assert.Equivalent(expected, actual);
	}

