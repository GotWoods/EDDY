		{
			AssignedIdentification = "l",
			ChangeOrResponseTypeCode = "Ds",
			Quantity = 1,
			QuantityLeftToReceive = 8,
			CompositeUnitOfMeasure = null,
			UnitPrice = 6,
			BasisOfUnitPriceCode = "Jk",
			ProductServiceIDQualifier = "Pt",
			ProductServiceID = "2",
			ProductServiceIDQualifier2 = "4l",
			ProductServiceID2 = "Y",
			ProductServiceIDQualifier3 = "Dp",
			ProductServiceID3 = "5",
			ProductServiceIDQualifier4 = "Hd",
			ProductServiceID4 = "1",
