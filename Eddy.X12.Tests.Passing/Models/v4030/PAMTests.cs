	[Fact]
	public void Parse_ShouldReturnCorrectObject()
	{
		string x12Line = "PAM*SA*9**E*2*yR*XeZ*Uwiohtbi*oNCU*U5E*sji3278y*JLhs*p*6*5";

		var expected = new PAM_PeriodAmount()
		{
			QuantityQualifier = "SA",
			Quantity = 9,
			CompositeUnitOfMeasure = null,
			AmountQualifierCode = "E",
			MonetaryAmount = 2,
			UnitOfTimePeriodOrInterval = "yR",
			DateTimeQualifier = "XeZ",
			Date = "Uwiohtbi",
			Time = "oNCU",
			DateTimeQualifier2 = "U5E",
			Date2 = "sji3278y",
			Time2 = "JLhs",
			PercentQualifier = "p",
			Percent = 6,
			YesNoConditionOrResponseCode = "5",
		};

		var actual = Map.MapObject<PAM_PeriodAmount>(x12Line, MapOptionsForTesting.x12DefaultEndsWithNewline);
		Assert.Equivalent(expected, actual);
	}

	[Theory]
	[InlineData("", 0, true)]
	[InlineData("E", 2, true)]
	[InlineData("E", 0, false)]
	[InlineData("", 2, false)]
	public void Validation_AllAreRequiredAmountQualifierCode(string amountQualifierCode, decimal monetaryAmount, bool isValidExpected)
	{
		var subject = new PAM_PeriodAmount();
		//Required fields
		//Test Parameters
		subject.AmountQualifierCode = amountQualifierCode;
		if (monetaryAmount > 0) 
			subject.MonetaryAmount = monetaryAmount;
		//If one filled, all required
		if(!string.IsNullOrEmpty(subject.PercentQualifier) || !string.IsNullOrEmpty(subject.PercentQualifier) || subject.Percent > 0)
		{
			subject.PercentQualifier = "p";
			subject.Percent = 6;
		}
		//If one, at least one
		if(!string.IsNullOrEmpty(subject.DateTimeQualifier2) || !string.IsNullOrEmpty(subject.DateTimeQualifier2) || !string.IsNullOrEmpty(subject.Date2) || !string.IsNullOrEmpty(subject.Time2))
		{
			subject.DateTimeQualifier2 = "U5E";
			subject.Date2 = "sji3278y";
			subject.Time2 = "JLhs";
		}
		TestHelper.CheckValidationResults(subject, isValidExpected, ErrorCodes.IfOneIsFilledAllAreRequired);
	}

