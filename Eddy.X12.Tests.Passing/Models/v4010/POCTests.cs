	public void Parse_ShouldReturnCorrectObject()
	{
		string x12Line = "POC*q*wU*1*6**3*Ru*lW*b*fo*W*Qc*z*av*T*qq*a*Uo*L*bf*C*17*d*0a*R*co*9";

		var expected = new POC_LineItemChange()
		{
			AssignedIdentification = "q",
			ChangeOrResponseTypeCode = "wU",
			QuantityOrdered = 1,
			QuantityLeftToReceive = 6,
			CompositeUnitOfMeasure = null,
			UnitPrice = 3,
			BasisOfUnitPriceCode = "Ru",
			ProductServiceIDQualifier = "lW",
			ProductServiceID = "b",
			ProductServiceIDQualifier2 = "fo",
			ProductServiceID2 = "W",
			ProductServiceIDQualifier3 = "Qc",
			ProductServiceID3 = "z",
			ProductServiceIDQualifier4 = "av",
			ProductServiceID4 = "T",
			ProductServiceIDQualifier5 = "qq",
